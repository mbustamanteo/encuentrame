						<div class="title"><h3>Antecedentes</h3></div>
						<p>
							<span class="dropcap">S</span>egún una investigación realizada en el marco del Día Internacional del Niño Desaparecido, el año 2009, se descubrió que no se tiene información precisa sobre cuántos menores de edad están extraviados en Latinoamérica.
						</p>
						<p>
							En Chile, cada una hora se extravía un niño. Según Carabineros, el 90% del total de las denuncias por presunta desgracia  en un año son solucionadas. De 19.000 personas extraviadas, aproximadamente un tercio corresponden a menores de edad.
						</p>
						<p>
							En México, la APRENEM (Asociación Pro Recuperación de Niños Extraviados y Orientación de la Juventud) recibe aproximadamente 60 denuncias al mes, lo que, según ellos, no representa exactamente la realidad del país. Actualmente, no existe una estadística que muestre cuántos niños están extraviados, no se lleva una cuenta exacta de ello, al contrario, existen muchas posibles cifras, todas diferentes.
						</p>
						<p>
							En Argentina no existe un organismo que lleve la cuenta de niños extraviados, por lo que hoy no se sabe a ciencia cierta cuántos niños han desaparecido.
